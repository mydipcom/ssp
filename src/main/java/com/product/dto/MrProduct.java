public class MrProduct {
	
	private int id;
	
	private String prodid;
	
	private String pname;
	
	private String pbrand;
	
	private String pcat;
	
	private float price;
	
	private String url;
	
	private String image;
	
	private String editor_note;
	
	private String size_fit;
	
	private String details_care;
	
	private long createtime;
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getProdid() {
		return prodid;
	}

	public void setProdid(String prodid) {
		this.prodid = prodid;
	}

	public String getPname() {
		return pname;
	}

	public void setPname(String pname) {
		this.pname = pname;
	}

	public String getPbrand() {
		return pbrand;
	}

	public void setPbrand(String pbrand) {
		this.pbrand = pbrand;
	}

	public String getPcat() {
		return pcat;
	}

	public void setPcat(String pcat) {
		this.pcat = pcat;
	}

	public float getPrice() {
		return price;
	}

	public void setPrice(float price) {
		this.price = price;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public String getEditor_note() {
		return editor_note;
	}

	public void setEditor_note(String editor_note) {
		this.editor_note = editor_note;
	}

	public String getSize_fit() {
		return size_fit;
	}

	public void setSize_fit(String size_fit) {
		this.size_fit = size_fit;
	}

	public String getDetails_care() {
		return details_care;
	}

	public void setDetails_care(String details_care) {
		this.details_care = details_care;
	}

	public long getCreatetime() {
		return createtime;
	}

	public void setCreatetime(long createtime) {
		this.createtime = createtime;
	}

	

}
